#include <math.h>
#include <stdio.h>
#include <string.h>
#include <stdlib.h>
#include <assert.h>
#include <limits.h>
#include <stdbool.h>

int main() {
	int n;
	int primary = 0;
	int secondary = 0;
	int difference;
	int a_i;

	scanf("%d", &n);
	int a[n][n];

	for (a_i = 0; a_i < n; a_i++) {
		for (int a_j = 0; a_j < n; a_j++) {
			scanf("%d", &a[a_i][a_j]);
		}
	}

	for (a_i = 0; a_i < n; a_i++) {
		primary += a[a_i][a_i];
	}
	for (a_i = 0; a_i < n; a_i++) {
		secondary += a[(n - 1) - a_i][a_i];
	}

	difference = abs(primary - secondary);

	printf("%d", difference);


	return 0;
}

#include <math.h>
#include <stdio.h>
#include <string.h>
#include <stdlib.h>
#include <assert.h>
#include <limits.h>
#include <stdbool.h>

int describe(int a) {
	if (a < 0) return -1;
	if (a > 0) return 1;
	return 0;
}

int main() {
	int n;
	scanf("%d", &n);
	int arr[n];
	for (int arr_i = 0; arr_i < n; arr_i++) {
		scanf("%d", &arr[arr_i]);
	}

	//int n = 6;
	//int arr[6] = { -4, 3, -9, 0, 4, 1 };
	float ratioNeg = 0;
	float ratioPve = 0;
	float ratioZeros = 0;
	int countNeg = 0, countPve = 0, countZeros = 0;

	for (int i = 0; i < n; i++) 
	{
		switch (describe(arr[i])) 
		{
		case 0:
			countZeros++;
			break;
		case -1:
			countNeg++;
			break;
		case 1:
			countPve++;
			break;
		}
	}

	ratioPve = countPve / (float)n;
	ratioNeg = countNeg / (float)n;
	ratioZeros = countZeros / (float)n;

	printf("%.6f\n", ratioPve);
	printf("%.6f\n", ratioNeg);
	printf("%.6f\n", ratioZeros);

	return 0;
}
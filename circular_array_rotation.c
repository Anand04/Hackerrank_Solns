#include <math.h>
#include <stdio.h>
#include <string.h>
#include <stdlib.h>
#include <assert.h>
#include <limits.h>
#include <stdbool.h>

int shift(int* a, int n, int k, int m) 
{
	int index;

	index = n - k;
	index += m;

	if (index > n - 1)
	{
		index -= n;
	}

	return a[index];
}

void query(int* a, int n, int k, int q) 
{
	int a0;
	int m;

	for (a0 = 0; a0 < q; a0++)
	{
		scanf("%d", &m);

		printf("%d\n", (k == n)? a[m] : shift(&a[0], n, k, m));
	}
}

int main() 
{
	int n, k, q, *a, a_i;

	scanf("%d %d %d", &n, &k, &q);
	a = malloc(sizeof(int) * n);

	for (a_i = 0; a_i < n; a_i++) 
	{
		scanf("%d", &a[a_i]);
	}

	while (k > n)
	{
		k -= n;
	}

	query(&a[0], n, k, q);


	return EXIT_SUCCESS;
}